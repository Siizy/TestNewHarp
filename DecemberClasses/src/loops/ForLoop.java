package loops;

public class ForLoop {

	public static void main(String[] args) {
		// For loop		
		// while loop
		// do-while loop
		// for-each loop
        // loop mean repeat actions when a condition is met
		
		// variable declaration 
		// variable initialization
							
		for(int i = 0; i < 10 ; i++) {
												
				if(i == 6) {					
					continue;
				}
				System.out.println("Value of i is : " + i);												
		}
	}

}
